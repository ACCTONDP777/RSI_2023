using log4net;

namespace RSI.Models.Utility
{
    /// <summary>
    /// 提供簡化使用Log記錄
    /// 建議PM非必要請勿自行更改，以免class版本更新造成版本不符
    /// </summary>
    public class LogHelper
    {
        public static ILog GetLogger(string loggerName)
        {
            return log4net.LogManager.GetLogger(loggerName);
        }

    }
}
